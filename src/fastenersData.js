// Helper functions for fasteners
export const getCatalogSections = () => {
  return fastenersData.catalogSections;
};

export const getFeaturedFasteners = () => {
  return fastenersData.featuredProducts;
};

export const getFastenersCategories = () => {
  return Object.keys(fastenersData.categories);
};

export const getFastenersByCategory = (category) => {
  return fastenersData.categories[category] || [];
};

export const getAllFastenersProducts = () => {
  const allProducts = [];
  Object.entries(fastenersData.categories).forEach(([category, products]) => {
    products.forEach(product => {
      allProducts.push({
        category,
        name: product,
        type: 'fastener'
      });
    });
  });
  return allProducts;
};

export const getSectionByName = (sectionName) => {
  return fastenersData.catalogSections.find(section => section.name === sectionName);
};

export const getFeaturedProductByCategory = (categoryName) => {
  return fastenersData.featuredProducts.find(product => product.subCategory === categoryName);
};

